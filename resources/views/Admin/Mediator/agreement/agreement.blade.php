    <p>
        <h3>第五条  保密事项</h3>
        （一）本协议任何条款和本协议所涉及的客户相关信息，以及客户签署的期货经纪合同等所有资料均属于保密信息，甲乙双方均负有保密义务。<br>
        （二）任何一方未经对方书面正式许可，不得以任何理由，在任何场合泄露、披露本协议任何条款及本协议所涉及的客户的任何资料。否则，守约一方有权追究对方的违约责任。
    </p>

    <p>
        <h3>第六条  其他事项</h3>
        （一）乙方并非甲方的员工或关联机构，不纳入甲方组织机构和劳动人事编制，仅为独立承担责任的主体。<br>
        （二）乙方签约前已阅读并完全了解本协议，乙方同意并愿意遵守。<br>
        （三）甲方可根据国家政策的要求及市场情况对公司规章制度等进行相应调整，乙方在此承诺接受并遵守。否则，本协议将自乙方提出异议时自动终止。<br>
        （四）提前解除本协议时，双方应结清因履行本协议产生的债权债务，否则双方仍应遵守本协议约定的义务。<br>
        （五）居间人当月居间报酬低于100元的，甲方不予以发放居间报酬。<br>
        （六）居间协议到期后乙方尚未办理居间协议续签手续的，甲方视同乙方放弃续签的权利，同时取消居间人原先开发的客户关系，并停止核发居间协议逾期后产生的报酬。乙方过期后再办理续签手续的视同为新签，所有流程参照新签居间协议标准办理，居间报酬自居间协议生效起计算，过往客户居间关系及居间报酬不予追溯。<br>
        （七）如乙方未能完成本协议期内的承诺指标，甲方在本协议续签时有权下调续签协议的留存返佣比例。<br>
        （八）乙方在本协议有效期内申请解除合同的，一年内不得重新签订合同。乙方更换所属部门的，居间返还比例一年内不得提高。
    </p>

    <p>
        <h3>第七条  违约责任</h3>
        （一）在履行本协议过程中，任何一方均应遵守本协议条款及甲方制定的规章制度，任何一方违约，守约方均可要求解除协议。<br>
        （二）如果甲方无故延期支付酬金超过30天，乙方有权解除本协议，并要求甲方立即支付到期的酬金及相关利息。<br>
        （三）如果乙方在签订居间协议后，从事任何违法、违规或违约的活动，均由乙方单独承担责任，甲方不承担任何连带责任，同时甲方拥有对乙方暂停、缓发或扣除部分或全部酬金、不予续约或立即终止协议的权利。如乙方违法、违规或违约行为给甲方造成损失的，乙方应当承担赔偿责任。<br>
        （四）居间客户发生投诉，乙方不能积极配合甲方妥善解决的，或因客户投诉给甲方带来不利影响，甲方有权对乙方暂停、缓发或扣除部分或全部酬金、不予续约或立即终止协议，给甲方造成损失的，乙方应当承担。
    </p>

    <p>
        <h3>第八条  争议的解决及管辖</h3>
        因本协议或履行本协议产生的纠纷，双方应友好协商。协商不成时，可向甲方所在地人民法院起诉。
    </p>

